package com.oracle.javafx.scenebuilder.app.util;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.fail;

import java.io.File;
import java.io.IOException;
import java.io.UncheckedIOException;
import java.net.URL;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.List;

import org.junit.jupiter.api.Test;
import org.opentest4j.AssertionFailedError;

import com.oracle.javafx.scenebuilder.app.about.AboutWindowController;

class AppSettingsTest {

    @Test
    void that_scene_builder_version_is_obtained_correctly() {
        String expectedVersion = readSceneBuilderVersion();
        assertEquals(expectedVersion, AppSettings.getSceneBuilderVersion());
    }

    private String readSceneBuilderVersion() {
        // about.properties is generated by Maven
        URL resource = AboutWindowController.class.getResource("about.properties");
        if (null == resource) {
            fail("The required file resource \"about.properties\" was not found!");
        }
        Path file = new File(resource.getFile()).toPath();
        try {
            List<String> lines = Files.readAllLines(file);
            for (String line : lines) {
                if (line.startsWith("build.version=")) {
                    return line.split("=")[1];
                }
            }
        } catch (IOException e) {
            throw new UncheckedIOException(e);
        }
        throw new AssertionFailedError("About.properties does not contain the required \"build.version\" entry.");
    }

}
